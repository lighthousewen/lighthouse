def _get_system_prompt(persona: str = "deep_mirror") -> str:
    return SYSTEM_PROMPTS.get(persona, SYSTEM_PROMPTS["deep_mirror"])


async def stream_chat(
    messages: list[dict],
    persona: str = "deep_mirror",
    model: str = "deepseek-chat",
    extra_context: str = "",
) -> AsyncGenerator[str, None]:
    system_prompt = _get_system_prompt(persona)
    if extra_context:
        system_prompt = system_prompt + "\n\n---\n\n" + extra_context
    full_messages = [{"role": "system", "content": system_prompt}] + messages

    async with httpx.AsyncClient(timeout=httpx.Timeout(60.0, connect=10.0)) as client:
        async with client.stream(
            "POST",
            f"{settings.deepseek_base_url}/v1/chat/completions",
            headers={
                "Authorization": f"Bearer {settings.deepseek_api_key}",
                "Content-Type": "application/json",
            },
            json={
                "model": model,
                "messages": full_messages,
                "stream": True,
            },
        ) as response:
            response.raise_for_status()
            async for line in response.aiter_lines():
                if not line.startswith("data: "):
                    continue
                data_str = line[6:]
                if data_str.strip() == "[DONE]":
                    break
                try:
                    chunk = json.loads(data_str)
                    delta = chunk.get("choices", [{}])[0].get("delta", {})
                    content = delta.get("content", "")
                    if content:
                        yield content
                except json.JSONDecodeError:
                    continue
